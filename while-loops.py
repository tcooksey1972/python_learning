#What?  A while loop is a control flow structure in Python that allows a block of code to be executed repeatedly as long as a specified condition is evaluated as True
#When?  While loops are used when we dont know how long the code needs to run.  Condistions stop it.
#When?  Repititive task that are based on specific conditions or Dynamic conditions or external factors
#Why?  We use while loops for dynamic control flow(react to change & execute codd), effeciency(more effecient than loops when we dont know the exact number requred) and handling user input(iterations depends on input)

i = 1
while i <= 10:   #conditions for our loop
    print(i)  #code inside the loop

    i += 1   #incriment 'i' by one each run.

print("done with loop")


